a = 5
b = 3
c = 4

# Adição (+)
resultado = a + b + c
print(f"Soma: {resultado}")

# Subtração (-)
resultado = c - b
print(f"Subtracao: {resultado}")

# Multiplicação (*)
resultado = b * a
print(f"Divisao Inteira: {resultado}")

# Divisao (/)
resultado = a / b
print(f"Divisao: {resultado}")

# Divisao Inteira (//)
resultado = a // c
print(f"Divisao Inteira: {resultado}")

# Resto da Divisao (%)
resultado = a % b
print(f"Resto da Divisao: {resultado}")

# Exponencial (**)
resultado = a ** b
print(f"Exponencial: {resultado}")

